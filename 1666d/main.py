a = II()
for _ in range(a):
    s, t = I().split()
    f = 0
    for i in s[::-1]:
        if i == t[-1]:
            t = t[:-1]
        elif i in t:
            f = 1

        if not t:
            break
    print("NO" if t or f else "YES")


