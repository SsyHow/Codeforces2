a = II()
for _ in range(a):
    b = II()
    L = LII()

    for i in range(b - 1):
        k = min(L[i], L[i + 1])
        L[i] -= k
        L[i + 1] -= k
        if L[i] > L[i + 1]:
            print("NO")
            break
    else:
        print("YES")