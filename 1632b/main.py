a = II()
for _ in range(a):
    b = II()
    n = 1 << ((b - 1).bit_length() - 1)
    L = []
    for i in range(n - 1, -1, -1):
        L.append(i)
    for i in range(n, b):
        L.append(i)
    print(*L)

