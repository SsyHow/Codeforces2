a = II()
for _ in range(a):
    b = II()
    ans = []
    for i in range(1, 19):
        k = 10 ** i + 1
        if b % k == 0:
            ans.append(b // k)

    print(len(ans))
    if len(ans):
        print(*ans[::-1])
