a = II()
for _ in range(a):
    b = II()
    L = LII()
    L.sort()
    if b == 1:
        print(L[0])
        continue

    ans = L[0]

    for i in range(b - 1):
        ans = max(ans, L[i + 1] - L[i])
    print(ans)