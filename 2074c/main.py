t = II()
for _ in range(t):
    b = II()
    ans = -1
    for i in range(30):
        for j in range(30):
            y = (1<<i) | (1 << j)
            if y < b and y + b > (b ^ y) and y + (b ^ y) > b:
                ans = y
    print(ans)